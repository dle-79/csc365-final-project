create table
  public.fridge (
    user_id bigint not null,
    ingredient_id bigint not null,
    quantity bigint null,
    constraint fridge_pkey primary key (ingredient_id, user_id)
  ) tablespace pg_default;

create table
  public.ingredient (
    ingredient_id bigint generated by default as identity,
    name text not null,
    vegan boolean null,
    vegetarian boolean null,
    paleo boolean null,
    calories bigint null,
    protein bigint null,
    carbs bigint null,
    fat bigint null,
    aisle text null,
    quantity text null,
    constraint ingredient_pkey primary key (ingredient_id)
  ) tablespace pg_default;

  create table
  public.recipe (
    recipe_id bigint generated by default as identity,
    sku text not null,
    name text null,
    protein bigint null,
    calories bigint null,
    vegan boolean null,
    vegetarian boolean null,
    paleo boolean null,
    country_origin text null,
    meal_type text null,
    carbs bigint null,
    servings integer null,
    time_to_make integer null,
    steps text null,
    num_ingredient bigint null,
    constraint recipe_pkey primary key (recipe_id)
  ) tablespace pg_default;

  create table
  public.recipe_ingredients (
    recipe_id bigint generated by default as identity,
    ingredient_id bigint not null,
    quantity real null,
    constraint recipe_ingredients_pkey primary key (recipe_id, ingredient_id),
    constraint recipe_ingredients_ingredient_id_fkey foreign key (ingredient_id) references ingredient (ingredient_id),
    constraint recipe_ingredients_recipe_id_fkey foreign key (recipe_id) references recipe (recipe_id)
  ) tablespace pg_default;

  create table
  public.shopping_list (
    user_id bigint generated by default as identity,
    ingredient_id bigint not null,
    quantity bigint null,
    purchase boolean null,
    constraint shopping_list_pkey primary key (user_id, ingredient_id)
  ) tablespace pg_default;

  create table
  public.users (
    user_id bigint generated by default as identity,
    name text not null,
    constraint user_pkey primary key (user_id)
  ) tablespace pg_default;

